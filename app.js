function pickFeedbackThemes(entryText) {
  const scored = feedbackThemes
    .map((theme) => {
      const score = theme.keywords.reduce((sum, keyword) => {
        return sum + (entryText.includes(keyword) ? 1 : 0);
      }, 0);

      return { ...theme, score };
    })
    .sort((a, b) => b.score - a.score);

  if (scored[0].score === 0) {
    return [feedbackThemes[0], feedbackThemes[1]];
  }

  const primary = scored[0];
  const secondary = scored.find((theme) => theme.id !== primary.id && theme.score > 0) || scored[1] || primary;
  return [primary, secondary];
}

function buildAutoFeedback(entry) {
  const rawText = `${entry.title} ${entry.observation} ${entry.reference} ${entry.stop} ${entry.route}`.toLowerCase();
  const [primaryTheme, secondaryTheme] = pickFeedbackThemes(rawText);
  const groebnerAnchor = inferGroebnerAnchor(rawText);
  const wiedmerAnchor = inferWiedmerAnchor(rawText);

  const strength =
    `Dein Beitrag ist stark, weil er ${primaryTheme.strength}. ` +
    `Du formulierst damit nicht nur ein Stimmungsbild, sondern ein analytisches Problem mit gesellschaftlicher Reichweite.`;

  const textBridge =
    `${wiedmerAnchor} ` +
    `Bei Enzensberger wird daraus das Kernparadoxon: Der touristische Zugriff bedroht genau das, was er als Ferne und Erholung verspricht. ` +
    `${groebnerAnchor}`;

  const critical =
    `Vertiefe nun ${primaryTheme.critical}. ` +
    `Nimm dafür mindestens eine zweite Primärtextstelle und kontrastiere sie mit ${secondaryTheme.label.toLowerCase()}, damit dein Argument weniger linear und belastbarer wird.`;

  const impulse =
    `${primaryTheme.impulse} ` +
    `Bleibe mutig in der Zuspitzung, aber belege jede Zuspitzung sichtbar am Text.`;

  return {
    strength,
    textBridge,
    critical,
    impulse,
    generatedAt: toIsoNow(),
    primaryTheme: primaryTheme.label,
    secondaryTheme: secondaryTheme.label
  };
}

