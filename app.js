function renderRouteNav() {
  const route = currentRoute();
  const fragment = document.createDocumentFragment();

  route.stops.forEach((stop, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "route-stop";
    button.innerHTML = `<strong>${index + 1}. ${escapeHtml(stop.title)}</strong><span>${escapeHtml(stop.subtitle)}</span>`;

    if (isStopDone(route.id, index)) {
      button.classList.add("done");
    }

    if (index === activeStopIndex) {
      button.classList.add("active");
    }

    button.addEventListener("click", () => setActiveStop(index));
    fragment.append(button);
  });

  els.routeNav.innerHTML = "";
  els.routeNav.append(fragment);
}

function getPrimaryTextLinks(stop) {
  const haystack = `${stop.title} ${stop.subtitle} ${stop.focus} ${stop.textLoop} ${stop.source}`.toLowerCase();
  const links = [];

  if (haystack.includes("blick") || haystack.includes("abu dhabi")) {
    links.push(primaryTextLibrary.blick);
  }
  if (haystack.includes("wiedmer") || haystack.includes("flut") || haystack.includes("stagen")) {
    links.push(primaryTextLibrary.wiedmer);
  }
  if (haystack.includes("enzensberger")) {
    links.push(primaryTextLibrary.enzensberger);
  }
  if (
    haystack.includes("abgefahren") ||
    haystack.includes("kap. 4") ||
    haystack.includes("kap. 5") ||
    haystack.includes("kap. 6") ||
    haystack.includes("kap. 7") ||
    haystack.includes("hyperdorf")
  ) {
    links.push(primaryTextLibrary.abgefahren);
  }
  if (haystack.includes("ferienmüde") || haystack.includes("urlaub") || haystack.includes("stressschleife")) {
    links.push(primaryTextLibrary.ferienmuede);
  }

  const seen = new Set();
  return links.filter((link) => {
    if (seen.has(link.href)) {
      return false;
    }
    seen.add(link.href);
    return true;
  });
}

function buildSourceLinksHtml(links) {
  return links
    .map((link) => {
      const resolvedLink = resolveSourceLink(link);
      const external = resolvedLink.resolvedHref.startsWith("http");
      const classes = resolvedLink.isFallback ? "source-link is-fallback" : "source-link";
      const title = resolvedLink.isFallback
        ? ' title="Lokale PDF fehlt in dieser Version. Es wird automatisch der Ersatzpfad geöffnet."'
        : "";

      return `<a class="${classes}" href="${escapeHtml(resolvedLink.resolvedHref)}" target="${external ? "_blank" : "_self"}"${external ? ' rel="noreferrer"' : ""}${title}>${escapeHtml(resolvedLink.label)}</a>`;
    })
    .join("");
}

