function pickFeedbackThemes(entryText) {
  const scored = feedbackThemes
    .map((theme) => {
      const score = theme.keywords.reduce((sum, keyword) => {
        return sum + (entryText.includes(keyword) ? 1 : 0);
      }, 0);

      return { ...theme, score };
    })
    .sort((a, b) => b.score - a.score);

  if (scored[0].score === 0) {
    return [feedbackThemes[0], feedbackThemes[1]];
  }

  const primary = scored[0];
  const secondary = scored.find((theme) => theme.id !== primary.id && theme.score > 0) || scored[1] || primary;
  return [primary, secondary];
}

