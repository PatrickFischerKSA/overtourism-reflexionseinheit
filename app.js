function addTeacherComment(entryId, text) {
  const trimmed = text.trim();
  if (!trimmed) return;

  const entry = blogEntries.find((candidate) => candidate.id === entryId);
  if (!entry) return;

  entry.teacherComments.unshift({
    id: `${Date.now()}-${Math.random().toString(16).slice(2)}`,
    text: trimmed,
    createdAt: toIsoNow()
  });

  saveBlogEntries();
  renderBlogEntries();
}

