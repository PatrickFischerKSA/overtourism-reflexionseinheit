function createBlogEntry(entryData) {
  const entry = {
    id: `${Date.now()}-${Math.random().toString(16).slice(2)}`,
    route: entryData.route,
    stop: entryData.stop,
    title: entryData.title,
    observation: entryData.observation,
    reference: entryData.reference,
    createdAt: toIsoNow(),
    teacherComments: []
  };

  entry.feedback = buildAutoFeedback(entry);
  blogEntries.unshift(entry);
  saveBlogEntries();
  renderBlogEntries();
}

function loadBlogEntries() {
  try {
    const raw = localStorage.getItem(blogStorageKey);
    if (!raw) return;
    const parsed = JSON.parse(raw);
    if (Array.isArray(parsed)) {
      blogEntries = parsed;
    }
  } catch {
    blogEntries = [];
  }

  normalizeBlogEntries();
  saveBlogEntries();
}

function renderFeedbackBlock(entry) {
  if (!entry.feedback) {
    return "";
  }

  return `
    <section class="auto-feedback">
      <p class="label">Generiertes Textfeedback</p>
      <p><strong>Stärke:</strong> ${escapeHtml(entry.feedback.strength)}</p>
      <p><strong>Textbrücke:</strong> ${escapeHtml(entry.feedback.textBridge)}</p>
      <p><strong>Kritische Vertiefung:</strong> ${escapeHtml(entry.feedback.critical)}</p>
      <p><strong>Gedankenimpuls:</strong> ${escapeHtml(entry.feedback.impulse)}</p>
      <p class="meta">Generiert: ${new Date(entry.feedback.generatedAt).toLocaleString("de-CH")} | Fokus: ${escapeHtml(entry.feedback.primaryTheme || "Paradoxon")} / ${escapeHtml(entry.feedback.secondaryTheme || "Vertiefung")}</p>
    </section>
  `;
}

function renderTeacherComments(comments) {
  if (!comments.length) {
    return "<p class='meta'>Noch kein Lehrerkommentar.</p>";
  }

  const items = comments
    .map((comment) => {
      return `<li><strong>${new Date(comment.createdAt).toLocaleString("de-CH")}:</strong> ${formatMultiline(comment.text)}</li>`;
    })
    .join("");

  return `<ul class="teacher-comments">${items}</ul>`;
}

function deleteBlogEntry(id) {
  blogEntries = blogEntries.filter((entry) => entry.id !== id);
  saveBlogEntries();
  renderBlogEntries();
}

