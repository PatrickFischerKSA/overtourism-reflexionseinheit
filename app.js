function numberOf(input) {
  return Number.parseFloat(input.value) || 0;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function calcTravelProfile() {
  const trips = numberOf(els.tripCount);
  const shortFlights = numberOf(els.shortFlights);
  const longFlights = numberOf(els.longFlights);
  const sehnsucht = numberOf(els.motSehnsucht);
  const rest = numberOf(els.motRest);
  const stressFeeling = numberOf(els.motStress);
  const display = numberOf(els.motDisplay);
  const encounter = numberOf(els.motEncounter);

  const gain = clamp(sehnsucht * 2 + rest * 2.2 + encounter * 1.8 - stressFeeling * 0.4, 0, 100);
  const stress = clamp(stressFeeling * 5 + trips * 1.8 + shortFlights * 2 + longFlights * 3 + display * 2.1, 0, 100);
  const local = clamp(trips * 2.6 + shortFlights * 1.7 + longFlights * 2.6 + display * 2.4, 0, 100);
  const eco = clamp(shortFlights * 8 + longFlights * 20 + trips * 1.2, 0, 100);

  els.gainScore.textContent = Math.round(gain);
  els.stressScore.textContent = Math.round(stress);
  els.localCostScore.textContent = Math.round(local);
  els.ecoScore.textContent = Math.round(eco);

  const paradoxIndex = gain - stress * 0.55 - local * 0.75 - eco * 0.8;

  if (paradoxIndex > 5) {
    els.scoreInterpretation.textContent =
      "Dein Gewinn ist hoch. Pruefe mit Enzensberger und Groebner, welche Lasten dafuer ausgelagert werden.";
  } else if (paradoxIndex > -14) {
    els.scoreInterpretation.textContent =
      "Ambivalente Zone: Sehnsucht und Erschoepfung liegen dicht beieinander. Arbeite mit Kap. 6 und Ferienmuede nach.";
  } else {
    els.scoreInterpretation.textContent =
      "Starke Paradox-Spannung: Die Erholung ist teuer erkauft. Formuliere konkrete Entzerrungs- und Reduktionsschritte.";
  }
}

function initTravelProfile() {
  const fields = [
    els.tripCount,
    els.shortFlights,
    els.longFlights,
    els.motSehnsucht,
    els.motRest,
    els.motStress,
    els.motDisplay,
    els.motEncounter
  ];

  fields.forEach((field) => field.addEventListener("input", calcTravelProfile));
  calcTravelProfile();
}

function saveBlogEntries() {
  localStorage.setItem(blogStorageKey, JSON.stringify(blogEntries));
}

function loadBlogEntries() {
  try {
    const raw = localStorage.getItem(blogStorageKey);
    if (!raw) return;
    const parsed = JSON.parse(raw);
    if (Array.isArray(parsed)) {
      blogEntries = parsed;
    }
  } catch {
    blogEntries = [];
  }

  normalizeBlogEntries();
  saveBlogEntries();
}

function renderFeedbackBlock(entry) {
  if (!entry.feedback) {
    return "";
  }

  return `
    <section class="auto-feedback">
      <p class="label">Generiertes Textfeedback</p>
      <p><strong>Staerke:</strong> ${escapeHtml(entry.feedback.strength)}</p>
      <p><strong>Textbruecke:</strong> ${escapeHtml(entry.feedback.textBridge)}</p>
      <p><strong>Kritische Vertiefung:</strong> ${escapeHtml(entry.feedback.critical)}</p>
      <p><strong>Gedankenimpuls:</strong> ${escapeHtml(entry.feedback.impulse)}</p>
      <p class="meta">Generiert: ${new Date(entry.feedback.generatedAt).toLocaleString("de-CH")} | Fokus: ${escapeHtml(entry.feedback.primaryTheme || "Paradoxon")} / ${escapeHtml(entry.feedback.secondaryTheme || "Vertiefung")}</p>
    </section>
  `;
}

function renderTeacherComments(comments) {
  if (!comments.length) {
    return "<p class='meta'>Noch kein Lehrerkommentar.</p>";
  }

  const items = comments
    .map((comment) => {
      return `<li><strong>${new Date(comment.createdAt).toLocaleString("de-CH")}:</strong> ${formatMultiline(comment.text)}</li>`;
    })
    .join("");

  return `<ul class="teacher-comments">${items}</ul>`;
}

function deleteBlogEntry(id) {
  blogEntries = blogEntries.filter((entry) => entry.id !== id);
  saveBlogEntries();
  renderBlogEntries();
}

function renderBlogEntries() {
  if (!blogEntries.length) {
    els.blogEntries.innerHTML = "<p class='card'>Noch keine Etappen gepostet.</p>";
    renderTeacherPanel();
    return;
  }

  els.blogEntries.innerHTML = blogEntries
    .map(
      (entry) => `
      <article class="blog-entry">
        <h3>${escapeHtml(entry.title)}</h3>
        <p class="meta">${escapeHtml(entry.route)} | ${escapeHtml(entry.stop)} | ${escapeHtml(entry.reference)} | ${new Date(entry.createdAt).toLocaleString("de-CH")}</p>
        <p>${escapeHtml(entry.observation)}</p>
        ${renderFeedbackBlock(entry)}
        <section class="teacher-comment-block">
          <p class="label">Lehrerkommentare</p>
          ${renderTeacherComments(entry.teacherComments)}
        </section>
        <button type="button" data-delete-id="${escapeHtml(entry.id)}">Loeschen</button>
      </article>
    `
    )
    .join("");

  const buttons = els.blogEntries.querySelectorAll("button[data-delete-id]");
  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      deleteBlogEntry(button.getAttribute("data-delete-id"));
    });
  });

  renderTeacherPanel();
}

function populateBlogStops() {
  const route = currentRoute();
  els.blogStop.innerHTML = route.stops
    .map((stop) => `<option value="${escapeHtml(stop.title)}">${escapeHtml(stop.title)}</option>`)
    .join("");
}

function initBlog() {
  populateBlogStops();
  loadBlogEntries();
  renderBlogEntries();

  els.blogForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const route = currentRoute();

    const entry = {
      id: `${Date.now()}-${Math.random().toString(16).slice(2)}`,
      route: route.name,
      stop: els.blogStop.value.trim(),
      title: els.blogTitle.value.trim(),
      observation: els.blogObservation.value.trim(),
      reference: els.blogReference.value.trim(),
      createdAt: toIsoNow(),
      teacherComments: []
    };

    if (!entry.stop || !entry.title || !entry.observation || !entry.reference) {
      return;
    }

    entry.feedback = buildAutoFeedback(entry);
    blogEntries.unshift(entry);
    saveBlogEntries();
    renderBlogEntries();
    els.blogForm.reset();
    populateBlogStops();
  });
}

function addTeacherComment(entryId, text) {
  const trimmed = text.trim();
  if (!trimmed) return;

  const entry = blogEntries.find((candidate) => candidate.id === entryId);
  if (!entry) return;

  entry.teacherComments.unshift({
    id: `${Date.now()}-${Math.random().toString(16).slice(2)}`,
    text: trimmed,
    createdAt: toIsoNow()
  });

  saveBlogEntries();
  renderBlogEntries();
}

function regenerateFeedback(entryId) {
  const entry = blogEntries.find((candidate) => candidate.id === entryId);
  if (!entry) return;

  entry.feedback = buildAutoFeedback(entry);
  saveBlogEntries();
  renderBlogEntries();
}

function renderTeacherPanel() {
  if (!els.teacherPanel) return;

  if (!isTeacherMode) {
    els.teacherPanel.hidden = true;
    els.teacherPanel.innerHTML = "";
    return;
  }

  els.teacherPanel.hidden = false;

  if (!blogEntries.length) {
    els.teacherPanel.innerHTML = "<p class='card'>Noch keine Blogeintraege fuer die Lehrerversion vorhanden.</p>";
    return;
  }

  els.teacherPanel.innerHTML = blogEntries
    .map((entry) => {
      return `
      <article class="teacher-entry">
        <p class="label">Lehrerversion</p>
        <h3>${escapeHtml(entry.title)}</h3>
        <p class="meta">${escapeHtml(entry.route)} | ${escapeHtml(entry.stop)} | ${escapeHtml(entry.reference)}</p>
        ${renderFeedbackBlock(entry)}
        <label for="comment-${escapeHtml(entry.id)}">Lehrerkommentar</label>
        <textarea id="comment-${escapeHtml(entry.id)}" rows="3" data-comment-input-id="${escapeHtml(entry.id)}" placeholder="Praezises, textnahes Feedback mit naechstem Schritt"></textarea>
        <div class="teacher-actions">
          <button type="button" data-add-comment-id="${escapeHtml(entry.id)}">Kommentar speichern</button>
          <button type="button" data-regenerate-feedback-id="${escapeHtml(entry.id)}">Feedback neu generieren</button>
        </div>
      </article>
      `;
    })
    .join("");

  const addButtons = els.teacherPanel.querySelectorAll("button[data-add-comment-id]");
  addButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const entryId = button.getAttribute("data-add-comment-id");
      const input = els.teacherPanel.querySelector(`textarea[data-comment-input-id="${entryId}"]`);
      if (!input) return;
      addTeacherComment(entryId, input.value);
    });
  });

  const regenerateButtons = els.teacherPanel.querySelectorAll("button[data-regenerate-feedback-id]");
  regenerateButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const entryId = button.getAttribute("data-regenerate-feedback-id");
      regenerateFeedback(entryId);
    });
  });
}

function syncTeacherModeUi() {
  if (!els.teacherModeBtn || !els.teacherModeState) return;

  els.teacherModeBtn.setAttribute("aria-pressed", String(isTeacherMode));
  els.teacherModeBtn.textContent = isTeacherMode ? "Lehrerversion deaktivieren" : "Lehrerversion aktivieren";
  els.teacherModeState.textContent = isTeacherMode
    ? "Lehrerversion aktiv: Kommentare und Feedbackstudio sichtbar."
    : "Schuelermodus aktiv.";
}

function initTeacherMode() {
  if (!els.teacherModeBtn) return;

  try {
    const raw = localStorage.getItem(teacherModeStorageKey);
    isTeacherMode = raw === "true";
  } catch {
    isTeacherMode = false;
  }

  syncTeacherModeUi();
  renderTeacherPanel();

  els.teacherModeBtn.addEventListener("click", () => {
    isTeacherMode = !isTeacherMode;
    localStorage.setItem(teacherModeStorageKey, String(isTeacherMode));
    syncTeacherModeUi();
    renderTeacherPanel();
  });
}

function initRoutes() {
  loadProgress();
  renderRouteTracks();
  renderRouteMeta();
  renderRouteNav();
  renderRouteDetail();
}

function init() {
  initRoutes();
  initParadoxConsole();
  initEncounters();
  initTravelProfile();
  initBlog();
  initTeacherMode();
}

init();
