function loadProgress() {
  try {
    const raw = localStorage.getItem(progressStorageKey);
    if (!raw) return;
    const parsed = JSON.parse(raw);
    if (parsed && typeof parsed === "object") {
      progressState = parsed;
    }
  } catch {
    progressState = {};
  }
}

function saveProgress() {
  localStorage.setItem(progressStorageKey, JSON.stringify(progressState));
}

function routeProgress(routeId) {
  if (!progressState[routeId]) {
    progressState[routeId] = {};
  }
  return progressState[routeId];
}

function isStopDone(routeId, index) {
  const map = progressState[routeId] || {};
  return Boolean(map[index]);
}

function setStopDone(routeId, index, done) {
  const map = routeProgress(routeId);
  map[index] = done;
  saveProgress();
}

